export function summarizeCatalog(gate) {
  const scoped = gate
    ? groupChatScenarioCatalog.filter((item) => item.gate === gate || item.gate === 'smoke')
    : groupChatScenarioCatalog;
  return {
    total: scoped.length,
    hardGate: scoped.filter((item) => item.hardGate).length,
    byArea: scoped.reduce((acc, item) => {
      acc[item.area] = (acc[item.area] || 0) + 1;
      return acc;
    }, {}),
    scenarios: scoped,
  };
}
