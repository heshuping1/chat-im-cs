export function scenarioCadenceRank(cadence) {
  if (cadence === 'daily') return 1;
  if (cadence === 'weekly') return 2;
  return 3;
}

export function scenariosForCadence(cadence) {
  const maxRank = scenarioCadenceRank(cadence);
  return pcAutomationFeatureMatrix.filter((scenario) => scenarioCadenceRank(scenario.cadence) <= maxRank);
}

export function summarizePcAutomationMatrix(cadence = 'weekly') {
  const scenarios = scenariosForCadence(cadence);
  return {
    cadence,
    total: scenarios.length,
    byArea: countBy(scenarios, 'area'),
    byPriority: countBy(scenarios, 'priority'),
    byAutomation: countBy(scenarios, 'automation'),
    hardGate: scenarios.filter((scenario) => scenario.priority === 'P0' || scenario.priority === 'P1').length,
    scenarios,
  };
}

function countBy(items, key) {
  return items.reduce((acc, item) => {
    const value = item[key] || 'unknown';
    acc[value] = (acc[value] || 0) + 1;
    return acc;
  }, {});
}
